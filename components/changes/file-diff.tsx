// ONE file's diff, self-contained: lazy raw-diff fetch, Prism highlighting + markEdits word-level
// emphasis, whitespace-only hunk folding, and the full line-comment machinery (hover-+, content
// re-anchoring, orphan strip, hold-to-batch). Used INLINE under an expanded overview card AND as
// the detail page's right pane — mount with key={file.path} so state resets per file.

type ViewType = "split" | "unified";

// react-diff-view's tokenize expects refractor.highlight() to return an ARRAY of hast nodes (its
// v3 API); refractor v5 returns a Root, so unwrap to .children.
const refractorAdapter = {
  highlight: (value: string, lang: string) => refractor.highlight(value, lang).children,
};

// Prism token palette (dark) for the tokenized diff. Injected as a runtime <style> because
