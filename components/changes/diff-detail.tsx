// Inline composer opened by clicking a diff line.
function ComposerBox({
  value,
  onChange,
  onSubmit,
  onCancel,
}: {
  value: string;
  onChange: (v: string) => void;
  onSubmit: () => void;
  onCancel: () => void;
}) {
  return (
    <div className="mt-1">
      <textarea
        autoFocus
        value={value}
        onChange={(e) => onChange(e.target.value)}
        onKeyDown={(e) => {
          if (e.key === "Enter" && (e.metaKey || e.ctrlKey)) {
            e.preventDefault();
            onSubmit();
          } else if (e.key === "Escape") {
            e.preventDefault();
            onCancel();
          }
        }}
        rows={2}
        placeholder="Comment on this line — the agent gets it at its next edit (⌘/Ctrl+Enter to send)"
        className="w-full resize-y rounded border border-white/10 bg-background px-2 py-1.5 text-[12px] leading-snug outline-none focus:border-[#ff7a45]/40"
      />
      <div className="mt-1 flex items-center gap-2">
        <button
          type="button"
          onClick={onSubmit}
          disabled={!value.trim()}
          className={cn(
            "rounded-md px-2 py-1 text-[11px] font-semibold transition-colors",
            value.trim() ? "bg-[#ff7a45]/20 text-[#ff7a45] hover:bg-[#ff7a45]/30" : "text-muted-foreground opacity-50",
          )}
        >
          Comment
        </button>
        <button
          type="button"
          onClick={onCancel}
          className="rounded-md px-2 py-1 text-[11px] text-muted-foreground transition-colors hover:text-foreground"
        >
          Cancel
        </button>
      </div>
    </div>
  );
}

const TONE: Record<GroupTone, { text: string; icon?: boolean }> = {
  plan: { text: "text-emerald-300/80" },
  warn: { text: "text-amber-300", icon: true },
  muted: { text: "text-muted-foreground" },
};

function TreeGroup({
  label,
  tone,
  files,
  onSelect,
  defaultOpen = true,
  emptyNote,
}: {
  label: string;
  tone: GroupTone;
  files: ChangedFile[];
  onSelect: (path: string) => void;
  defaultOpen?: boolean;
  emptyNote?: string;
}) {
  const [open, setOpen] = useState(defaultOpen);
  const t = TONE[tone];
  return (
    <div className="mb-1">
      <button
        type="button"
        onClick={() => setOpen((o) => !o)}
        className={cn("flex w-full items-center gap-1.5 rounded px-2 py-1 text-[10px] font-semibold uppercase tracking-wide transition-colors hover:bg-white/[0.04]", t.text)}
      >
        <ChevronRight className={cn("size-3 shrink-0 transition-transform", open && "rotate-90")} />
        {t.icon && <AlertTriangle className="size-3 shrink-0" />}
        <span className="truncate">{label}</span>
        <span className="ml-auto tabular-nums opacity-70">{files.length}</span>
      </button>
      {open &&
        (files.length > 0 ? (
          <FileTree files={toTree(files)} onSelect={onSelect} />
        ) : emptyNote ? (
          <p className="px-2 py-1 pl-6 text-[10px] text-muted-foreground/70">{emptyNote}</p>
        ) : null)}
    </div>
  );
}

function DiffNote({
  children,
  icon,
  onOpen,
}: {
  children: React.ReactNode;
  icon?: boolean;
  onOpen?: () => void;
}) {
  return (
    <div className="flex items-center gap-2 px-4 py-6 text-[12px] text-muted-foreground">
      {icon && <FileWarning className="size-4 shrink-0 text-amber-300/80" />}
      <span>{children}</span>
      {onOpen && (
        <button
          type="button"
          onClick={onOpen}
          className="rounded-full border border-white/12 px-2 py-0.5 text-[11px] text-foreground transition-colors hover:bg-white/[0.06]"
        >
          Open in editor
        </button>
      )}
    </div>
  );
}
