function toTreeStatus(s: ChangedFile["status"]): FileStatus {
  return s === "added" ? "added" : s === "deleted" ? "deleted" : "modified";
}
function toTree(files: ChangedFile[]): FileLeafInput[] {
  return files.map((f) => ({ path: f.path, status: toTreeStatus(f.status), meta: `+${f.additions} −${f.deletions}` }));
}
export function openInEditor(path: string) {
  fetch(`/api/open?path=${encodeURIComponent(path)}`).catch(() => {});
}

// A hunk whose every changed line is whitespace-only reflow — folded by default.
function hunkIsFormattingOnly(h: HunkData): boolean {
  const removed = h.changes.filter((c) => c.type === "delete").map((c) => c.content);
  const added = h.changes.filter((c) => c.type === "insert").map((c) => c.content);
  return (removed.length > 0 || added.length > 0) && isWhitespaceOnlyHunk(removed, added);
}

export function DiffDetail({
  repo,
  files,
  touched,
  contract,
  initialPath,
  onBack,
}: {
  repo: boolean;
  files: ChangedFile[];
  touched: string[];
  contract?: { declaredFiles: string[]; authorizedExtras: string[] } | null;
  initialPath: string | null;
  onBack: () => void;
}) {
  const sessionSet = useMemo(() => new Set(touched), [touched]);
  const inSession = (f: ChangedFile) => sessionSet.has(f.path) || (!!f.oldPath && sessionSet.has(f.oldPath));

  // The active plan's file set (declaredFiles ∪ authorizedExtras). Scoping only kicks in when the
  // contract actually names files — an empty contract falls back to the session view below.
  const onPlanSet = useMemo(
    () => (contract ? new Set([...contract.declaredFiles, ...contract.authorizedExtras]) : null),
    [contract],
  );
  const hasScope = !!onPlanSet && onPlanSet.size > 0;
  const inPlan = (f: ChangedFile) => !!onPlanSet && (onPlanSet.has(f.path) || (!!f.oldPath && onPlanSet.has(f.oldPath)));

  const [mode, setMode] = useState<ViewType>("split");
  const [selected, setSelected] = useState<string | null>(initialPath);

  // ── Collapsible + resizable file-list sidebar ────────────────────────────────
  const [sidebarOpen, setSidebarOpen] = useState<boolean>(() => {
    if (typeof window === "undefined") return true;
    try {
      return window.localStorage.getItem(SIDEBAR_OPEN_KEY) !== "0";
    } catch {
      return true;
    }
  });
  const [sidebarW, setSidebarW] = useState<number>(() => {
    if (typeof window === "undefined") return 320;
    try {
      const v = Number(window.localStorage.getItem(SIDEBAR_W_KEY));
      return v ? Math.max(SIDEBAR_MIN, Math.min(SIDEBAR_MAX, v)) : 320;
    } catch {
      return 320;
    }
  });
  const toggleSidebar = useCallback(() => {
    setSidebarOpen((o) => {
      const next = !o;
      try {
        localStorage.setItem(SIDEBAR_OPEN_KEY, next ? "1" : "0");
      } catch {
        /* ignore */
      }
      return next;
    });
  }, []);
  const containerRef = useRef<HTMLDivElement | null>(null);
  const draggingRef = useRef(false);
  const onResizeDown = useCallback((e: React.PointerEvent) => {
    e.preventDefault();
    draggingRef.current = true;
    document.body.style.cursor = "col-resize";
    document.body.style.userSelect = "none";
  }, []);
  useEffect(() => {
    const onMove = (e: PointerEvent) => {
      if (!draggingRef.current || !containerRef.current) return;
      const left = containerRef.current.getBoundingClientRect().left;
      setSidebarW(Math.max(SIDEBAR_MIN, Math.min(SIDEBAR_MAX, e.clientX - left)));
    };
    const onUp = () => {
      if (!draggingRef.current) return;
      draggingRef.current = false;
      document.body.style.cursor = "";
      document.body.style.userSelect = "";
      try {
        localStorage.setItem(SIDEBAR_W_KEY, String(sidebarW));
      } catch {
        /* ignore */
      }
    };
    window.addEventListener("pointermove", onMove);
    window.addEventListener("pointerup", onUp);
    return () => {
      window.removeEventListener("pointermove", onMove);
      window.removeEventListener("pointerup", onUp);
    };
  }, [sidebarW]);

  // Contract mode: partition changes into On-plan, Strayed (off-plan but edited THIS session — the
  // divergence signal), and Other (pre-existing uncommitted noise).
  const groups = useMemo(() => {
    if (!hasScope) return null;
    return {
      onPlan: files.filter(inPlan),
      strayed: files.filter((f) => !inPlan(f) && inSession(f)),
      other: files.filter((f) => !inPlan(f) && !inSession(f)),
    };
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [files, hasScope, onPlanSet, sessionSet]);

  // Session-mode scope pill (only when there's no contract to scope by).
  const hasSession = files.some(inSession);
  const [scope, setScope] = useState<"session" | "all">("session");
  const effScope = scope === "session" && !hasSession ? "all" : scope;

  // Flat ordered list for selection default + lookup.
  const visibleList = useMemo(() => {
    if (groups) return [...groups.onPlan, ...groups.strayed, ...groups.other];
    return effScope === "all" ? files : files.filter(inSession);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [groups, files, effScope, sessionSet]);

  const byPath = useMemo(() => new Map(files.map((f) => [f.path, f])), [files]);
  const activePath = selected && visibleList.some((f) => f.path === selected) ? selected : visibleList[0]?.path ?? null;
  const active = activePath ? byPath.get(activePath) ?? null : null;
  const activeStrayed = !!active && !!groups && groups.strayed.some((f) => f.path === active.path);

  // Lazily fetch the selected file's raw diff. Re-fetches on selection change OR when the file list
  // changes (live refresh). Readiness derives from `raw.path === active.path` (no sync setState).
  const [raw, setRaw] = useState<{ path: string; diff: string } | null>(null);
  useEffect(() => {
    if (!active || active.binary || active.tooLarge) return;
    const path = active.path;
    const old = active.oldPath ?? "";
    let ignore = false;
    const ws = currentTabWs();
    fetch(`/api/changes?path=${encodeURIComponent(path)}&old=${encodeURIComponent(old)}`, {
      cache: "no-store",
      headers: ws ? { "x-beacon-workspace": ws } : undefined,
    })
      .then((r) => r.json() as Promise<FileDiff>)
      .then((c) => {
        if (!ignore) setRaw({ path, diff: c.diff ?? "" });
      })
      .catch(() => {});
    return () => {
      ignore = true;
    };
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [activePath, active?.binary, active?.tooLarge, files]);

  const parsed: FileData | null = useMemo(() => {
    if (!raw?.diff) return null;
    try {
      // A rename git renders as delete+add (below its similarity threshold) yields two blocks —
      // pick the one for the file we fetched, not blindly [0].
      const fs = parseDiff(raw.diff);
      return fs.find((f) => f.newPath === raw.path) ?? fs[0] ?? null;
    } catch {
      return null;
    }
  }, [raw]);

  // Syntax-highlight the diff via refractor (Prism) + word-level edit emphasis (markEdits).
  // Unknown languages fall back to a plain diff.
  const tokens = useMemo(() => {
    if (!parsed || !active) return undefined;
    try {
      const highlight = refractor.registered(active.lang);
      return tokenize(parsed.hunks, {
        highlight,
        refractor: refractorAdapter,
        language: active.lang,
        enhancers: [markEdits(parsed.hunks, { type: "block" })],
      });
    } catch {
      return undefined;
    }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [parsed, active?.lang]);
  const diffReady = raw && active && raw.path === active.path;

  // Whitespace-only hunks the user explicitly expanded (reset per file).
  const [showFormatting, setShowFormatting] = useState<Set<string>>(new Set());
  useEffect(() => {
    // eslint-disable-next-line react-hooks/set-state-in-effect
    setShowFormatting(new Set());
  }, [activePath]);

  // ── Line-comments on the diff → the running agent ────────────────────────────
  // Click a line to leave a comment; it's stored per file+line, and the PreToolUse guard hook
  // injects it as non-blocking context at the agent's next edit. Comments (re)load per active file.
  const [fileComments, setFileComments] = useState<DiffComment[]>([]);
  const [composer, setComposer] = useState<{ key: string; line: number; side: "old" | "new" } | null>(null);
  const [draft, setDraft] = useState("");
  const reloadComments = useCallback(async (path: string) => {
    const ws = currentTabWs();
    const r = (await fetch(`/api/changes/comment?file=${encodeURIComponent(path)}`, {
      cache: "no-store",
      headers: ws ? { "x-beacon-workspace": ws } : undefined,
    })
      .then((res) => res.json())
      .catch(() => null)) as { comments?: DiffComment[] } | null;
    setFileComments(r?.comments ?? []);
  }, []);
  useEffect(() => {
    // Deliberate reset-on-selection-change: a new file gets a clean composer + its own comments.
    // eslint-disable-next-line react-hooks/set-state-in-effect
    setComposer(null);
    setDraft("");
    if (activePath) void reloadComments(activePath);
    else setFileComments([]);
  }, [activePath, reloadComments]);

  const addComment = useCallback(async () => {
    if (!composer || !active || !draft.trim()) return;
    const ws = currentTabWs();
    await fetch("/api/changes/comment", {
      method: "POST",
      headers: { "content-type": "application/json", ...(ws ? { "x-beacon-workspace": ws } : {}) },
      body: JSON.stringify({ file: active.path, line: composer.line, side: composer.side, body: draft.trim() }),
    }).catch(() => {});
    setComposer(null);
    setDraft("");
    void reloadComments(active.path);
  }, [composer, active, draft, reloadComments]);

  const deleteComment = useCallback(
    async (id: string) => {
      const ws = currentTabWs();
      await fetch(`/api/changes/comment?id=${encodeURIComponent(id)}`, {
        method: "DELETE",
        headers: ws ? { "x-beacon-workspace": ws } : undefined,
      }).catch(() => {});
      if (active) void reloadComments(active.path);
    },
    [active, reloadComments],
  );

  // Map "side:line" → the diff's changeKey so stored comments (kept by line) can be placed as
  // widgets, and so a clicked line opens the composer on the right change.
  const changeKeyByLine = useMemo(() => {
    const m = new Map<string, string>();
    if (!parsed) return m;
    for (const h of parsed.hunks)
      for (const ch of h.changes) {
        const key = getChangeKey(ch);
        const nl = computeNewLineNumber(ch);
        if (nl && nl > 0) m.set(`new:${nl}`, key);
        const ol = computeOldLineNumber(ch);
        if (ol && ol > 0) m.set(`old:${ol}`, key);
      }
    return m;
  }, [parsed]);

  const gutterEvents = useMemo(
    () => ({
      onClick: (args: ChangeEventArgs) => {
        const ch = args.change as ChangeData | null;
        if (!ch) return;
        // Use the clicked SIDE when the event carries it (split view); fall back to change type.
        const side: "old" | "new" = args.side === "old" || (args.side === undefined && isDelete(ch)) ? "old" : "new";
        const line = side === "old" ? computeOldLineNumber(ch) : computeNewLineNumber(ch);
        if (!line || line <= 0) return;
        setComposer({ key: getChangeKey(ch), line, side });
        setDraft("");
      },
    }),
    [],
  );

  // One widget per line-key: the comments stored there + the composer if it targets that line.
  const widgets = useMemo(() => {
    const byKey = new Map<string, { comments: DiffComment[]; composer: boolean }>();
    const at = (k: string) => {
      let v = byKey.get(k);
      if (!v) {
        v = { comments: [], composer: false };
        byKey.set(k, v);
      }
      return v;
    };
    for (const c of fileComments) {
      const k = changeKeyByLine.get(`${c.side}:${c.line}`);
      if (k) at(k).comments.push(c);
    }
    if (composer) at(composer.key).composer = true;
    const w: Record<string, React.ReactNode> = {};
    for (const [k, v] of byKey) {
      w[k] = (
        <div className="border-y border-[#ff7a45]/20 bg-[#ff7a45]/[0.04] px-3 py-2">
          {v.comments.map((c) => (
            <CommentCard key={c.id} c={c} onDelete={() => void deleteComment(c.id)} />
          ))}
          {v.composer && (
            <ComposerBox
              value={draft}
              onChange={setDraft}
              onSubmit={() => void addComment()}
              onCancel={() => {
                setComposer(null);
                setDraft("");
              }}
            />
          )}
        </div>
      );
    }
    return w;
  }, [fileComments, changeKeyByLine, composer, draft, addComment, deleteComment]);

  // Below this sidebar width the header would wrap, so labels collapse to icons (title hides,
  // scope pill + plan badge become icon-only with the text moved to tooltips).
  const narrow = sidebarW < 300;
  const SCOPE_META = {
    session: { label: "This session", Icon: Clock, title: "Files edited this session" },
    all: { label: "All", Icon: List, title: "All uncommitted changes" },
  } as const;
  const scopePill = (
    <div className="ml-auto flex items-center gap-0.5 rounded-full border border-white/10 p-0.5">
      {(["session", "all"] as const).map((s) => {
        const { label, Icon, title } = SCOPE_META[s];
        const disabled = s === "session" && !hasSession;
        return (
          <button
            key={s}
            type="button"
            onClick={() => setScope(s)}
            disabled={disabled}
            className={cn(
              "flex items-center justify-center rounded-full text-[10px] font-medium transition-colors",
              narrow ? "size-5" : "px-2 py-0.5",
              effScope === s ? "bg-white/10 text-foreground" : "text-muted-foreground hover:text-foreground",
              disabled && "opacity-40",
            )}
            title={title}
          >
            {narrow ? <Icon className="size-3" /> : label}
          </button>
        );
      })}
    </div>
  );

  return (
    <div ref={containerRef} className="flex min-h-0 flex-1">
      <style>{PRISM_CSS}</style>
      {/* LEFT — changed-files tree (collapsible + drag-resizable) */}
      {sidebarOpen ? (
        <aside style={{ width: sidebarW }} className="flex min-w-0 shrink-0 flex-col border-r border-white/10 bg-background">
          <div className="flex items-center gap-2 px-3 pb-2 pt-3">
            <button
              type="button"
              onClick={onBack}
              title="Back to overview"
              className="flex size-5 shrink-0 items-center justify-center rounded text-muted-foreground/70 transition-colors hover:bg-white/[0.06] hover:text-foreground"
            >
              <ArrowLeft className="size-3.5" />
            </button>
            <button
              type="button"
              onClick={toggleSidebar}
              title="Hide the file list"
              className="flex size-5 shrink-0 items-center justify-center rounded text-muted-foreground/70 transition-colors hover:bg-white/[0.06] hover:text-foreground"
            >
              <PanelLeftClose className="size-3.5" />
            </button>
            <GitCompare className="size-4 shrink-0 text-[#ff7a45]" />
            {!narrow && <h1 className="text-sm font-semibold tracking-tight">Changes</h1>}
            <span className="text-[11px] tabular-nums text-muted-foreground">{visibleList.length}</span>
            {hasScope ? (
              <span
                className={cn(
                  "ml-auto flex items-center rounded-full border border-emerald-400/25 bg-emerald-400/10 font-medium text-emerald-300/90",
                  narrow ? "size-5 justify-center" : "gap-1 px-2 py-0.5 text-[10px]",
                )}
                title={`Scoped to this plan — ${onPlanSet!.size} file${onPlanSet!.size === 1 ? "" : "s"} in scope`}
              >
                {narrow ? <Target className="size-3" /> : "Plan-scoped"}
              </span>
            ) : repo && files.length > 0 ? (
              scopePill
            ) : null}
          </div>
          <div className="min-h-0 flex-1 overflow-y-auto px-1.5 pb-4">
            {!repo ? (
              <p className="px-2 py-4 text-[11px] text-muted-foreground">Not a git repository.</p>
            ) : files.length === 0 ? (
              <p className="px-2 py-4 text-[11px] leading-relaxed text-muted-foreground">
                No uncommitted changes yet — edits the agent makes as it executes the plan show up
                here live.
              </p>
            ) : groups ? (
              <>
                <TreeGroup label="On plan" tone="plan" files={groups.onPlan} onSelect={setSelected} emptyNote="No plan files changed yet." />
                {groups.strayed.length > 0 ? (
                  <TreeGroup label="Strayed" tone="warn" files={groups.strayed} onSelect={setSelected} />
                ) : groups.onPlan.length > 0 ? (
                  <p className="mb-1 px-2 py-1 text-[10px] text-emerald-400/70">✓ No strays — all session edits are on-plan.</p>
                ) : null}
                {groups.other.length > 0 && (
                  <TreeGroup label="Other uncommitted" tone="muted" files={groups.other} onSelect={setSelected} defaultOpen={false} />
                )}
              </>
            ) : (
              <FileTree files={toTree(visibleList)} onSelect={setSelected} />
            )}
          </div>
        </aside>
      ) : null}
      {sidebarOpen ? (
        // Drag-to-resize handle (a thin bar with a wide invisible hit area), same as /plan's split.
        <div
          onPointerDown={onResizeDown}
          className="group relative w-px shrink-0 cursor-col-resize bg-white/10 hover:bg-white/20"
          title="Drag to resize"
        >
          <div className="absolute inset-y-0 -left-1.5 right-[-6px] z-10" />
        </div>
      ) : (
        // Collapsed rail — a slim reopen affordance so the file list is one click away.
        <aside className="flex w-10 shrink-0 flex-col items-center gap-1 border-r border-white/10 bg-background py-2">
          <button
            type="button"
            onClick={onBack}
            title="Back to overview"
            className="flex size-7 items-center justify-center rounded-md text-muted-foreground transition-colors hover:bg-white/[0.06] hover:text-foreground"
          >
            <ArrowLeft className="size-4" />
          </button>
          <button
            type="button"
            onClick={toggleSidebar}
            title={`Show the file list · ${visibleList.length}`}
            className="flex size-7 items-center justify-center rounded-md text-muted-foreground transition-colors hover:bg-white/[0.06] hover:text-foreground"
          >
            <PanelLeftOpen className="size-4" />
          </button>
        </aside>
      )}

      {/* RIGHT — diff of the selected file */}
      <main className="flex min-w-0 flex-1 flex-col bg-background">
        {active ? (
          <>
            <div className="flex items-center gap-2 border-b border-white/10 px-4 py-2">
              {activeStrayed && (
                <span
                  className="flex shrink-0 items-center gap-1 rounded-full border border-amber-400/30 bg-amber-400/10 px-1.5 py-0.5 text-[10px] font-medium text-amber-300"
                  title="This file is outside the plan's declared scope"
                >
                  <AlertTriangle className="size-3" /> Strayed
                </span>
              )}
              <span className="truncate font-mono text-[12px] text-foreground/90" title={active.path}>
                {active.oldPath ? `${active.oldPath} → ${active.path}` : active.path}
              </span>
              <span className="shrink-0 text-[11px] tabular-nums text-emerald-400">+{active.additions}</span>
              <span className="shrink-0 text-[11px] tabular-nums text-rose-400">−{active.deletions}</span>
              {fileComments.length > 0 && (
                <span
                  className="flex shrink-0 items-center gap-1 rounded-full border border-[#ff7a45]/25 bg-[#ff7a45]/10 px-1.5 py-0.5 text-[10px] font-medium text-[#ff7a45]"
                  title="Line-comments on this file (sent to the agent at its next edit)"
                >
                  <MessageSquarePlus className="size-3" /> {fileComments.length}
                </span>
              )}
              <span className="hidden shrink-0 text-[10px] text-muted-foreground/45 lg:inline">
                click a line # to comment
              </span>
              <div className="ml-auto flex shrink-0 items-center gap-0.5">
                <div className="flex items-center gap-0.5 rounded-full border border-white/10 p-0.5">
                  <button
                    type="button"
                    onClick={() => setMode("split")}
                    title="Split view"
                    className={cn(
                      "flex size-6 items-center justify-center rounded-full transition-colors",
                      mode === "split" ? "bg-white/10 text-foreground" : "text-muted-foreground hover:text-foreground",
                    )}
                  >
                    <Columns2 className="size-3.5" />
                  </button>
                  <button
                    type="button"
                    onClick={() => setMode("unified")}
                    title="Unified view"
                    className={cn(
                      "flex size-6 items-center justify-center rounded-full transition-colors",
                      mode === "unified" ? "bg-white/10 text-foreground" : "text-muted-foreground hover:text-foreground",
                    )}
                  >
                    <Rows2 className="size-3.5" />
                  </button>
                </div>
                <button
                  type="button"
                  onClick={() => openInEditor(active.path)}
                  title="Open in editor"
                  className="flex size-6 items-center justify-center rounded-full text-muted-foreground transition-colors hover:bg-white/[0.06] hover:text-foreground"
                >
                  <ExternalLink className="size-3.5" />
                </button>
              </div>
            </div>
            <div className="min-h-0 flex-1 overflow-auto">
              {active.binary ? (
                <DiffNote icon>Binary file — no text diff.</DiffNote>
              ) : active.tooLarge ? (
                <DiffNote icon onOpen={() => openInEditor(active.path)}>
                  Diff too large to render here.
                </DiffNote>
              ) : !diffReady ? (
                <DiffNote>Loading diff…</DiffNote>
              ) : !parsed || parsed.hunks.length === 0 ? (
                <DiffNote>No textual changes.</DiffNote>
              ) : (
                <Diff
                  className="beacon-diff"
                  viewType={mode}
                  diffType={parsed.type}
                  hunks={parsed.hunks}
                  tokens={tokens}
                  widgets={widgets}
                  gutterEvents={gutterEvents}
                >
                  {(hunks) =>
                    hunks.map((h) =>
                      hunkIsFormattingOnly(h) && !showFormatting.has(h.content) ? (
                        <Decoration key={h.content}>
                          <div className="flex items-center gap-2 bg-white/[0.02] px-4 py-1 text-[11px] text-muted-foreground">
                            <span>· formatting-only hunk ({h.changes.filter((c) => c.type !== "normal").length} lines)</span>
                            <button
                              type="button"
                              onClick={() => setShowFormatting((s) => new Set([...s, h.content]))}
                              className="rounded-full border border-white/12 px-2 py-0.5 text-[10px] hover:bg-white/[0.06]"
                            >
                              show
                            </button>
                          </div>
                        </Decoration>
                      ) : (
                        <Hunk key={h.content} hunk={h} />
                      ),
                    )
                  }
                </Diff>
              )}
            </div>
          </>
        ) : (
          <div className="flex flex-1 items-center justify-center px-6 text-center text-sm text-muted-foreground">
            {!repo
              ? "Not a git repository."
              : files.length === 0
                ? "No uncommitted changes yet — edits the agent makes show up here live."
                : "Select a file to see its diff."}
          </div>
        )}
      </main>
    </div>
  );
}

