// The drill layer: changed-files tree (left, collapsible + drag-resizable) → git diff of the
// selected file (right). Reached from the Mission Control overview; Back returns there. Word-level
// edit emphasis via markEdits; whitespace-only hunks fold to a one-line row (skim-budget noise).
// When the active plan has a scope contract, the tree groups edits On-plan vs Strayed vs Other.

type ViewType = "split" | "unified";
type GroupTone = "plan" | "warn" | "muted";

const SIDEBAR_OPEN_KEY = "beacon:changes-sidebar-open";
const SIDEBAR_W_KEY = "beacon:changes-sidebar-w";
const SIDEBAR_MIN = 220;
const SIDEBAR_MAX = 620;

// react-diff-view's tokenize expects refractor.highlight() to return an ARRAY of hast nodes (its
// v3 API); refractor v5 returns a Root, so unwrap to .children.
const refractorAdapter = {
  highlight: (value: string, lang: string) => refractor.highlight(value, lang).children,
};

